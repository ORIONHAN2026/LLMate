  /// 强制使用工具获取数据，不要从历史聊天中提取
  static const String useToolsForData = '''## 📡 数据获取规则

**重要：所有数据必须通过工具实时获取，严禁从历史聊天记录中提取或复用。**

1. ❌ **禁止从历史聊天中提取数据**：不要引用或复述之前对话中出现过的数据、代码片段、文件内容。
2. ❌ **禁止依赖记忆中的数据**：即使你在之前的回复中看到过某个文件内容或数据，也必须重新通过工具获取。
3. ✅ **始终使用工具获取最新数据**：需要读取文件内容时，使用文件读取工具；需要查看项目结构时，使用目录浏览工具；需要执行命令时，使用命令行工具。
4. ✅ **数据准确性优先**：用户可能已修改了文件或数据，历史聊天中的内容可能已过时。始终以当前文件系统的实际内容为准。
