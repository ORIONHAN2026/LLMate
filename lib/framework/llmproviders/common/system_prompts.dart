  /// 核心行为规则（合并：工具使用 + 反幻觉 + 数据获取）
  /// 这是最重要的一条系统提示，放在用户消息前紧邻位置。
  static const String coreRules = '''## ⚡ 核心规则

1. **工具优先**：回答需要文件内容、目录结构、代码片段或数据时，必须先调用工具获取。严禁在未调用工具的情况下编造文件名、路径、代码内容或数据。
2. **操作验证**：声称完成了任何操作（修改文件、执行命令等），必须确实通过工具执行过。严禁假装执行。
